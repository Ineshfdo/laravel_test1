<?php

use App\Models\Product;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Route;
use App\Http\Controllers\UserController;
use App\Http\Controllers\ProductController;

// Common Resource Routes:

// index   – Show all products
// show    – Show single product // Use at the end of the routes
// create  – Show form to create new product
// store   – Store new created product
// edit    – Show form to edit product
// update  – Update product
// destroy – Delete product


// Show All the Products

// Define the route for the homepage and call the index function in the ProductController
Route::get('/', [ProductController::class, 'index']);





















// Show Create Form                              ->middleware('auth') can only assess when user logged in
 

Route::get('/products/create', [ProductController::class, 'create'])->middleware('auth');






















































// Store Create Form Data
Route::post('/products', [ProductController::class, 'store'])->middleware('auth');

// Route::post('/products', [ProductController::class, 'store'])->middleware('auth');













































// Show Edit Form
 
Route::get('/products/{product}/edit', [ProductController::class, 'edit'])->middleware('auth');
















































// Edit Submit to Update
Route::put('/products/{product}', [ProductController::class, 'update'])->middleware('auth');

// Route::put('/products/{product}', [ProductController::class, 'update'])->middleware('auth');

































// Delete Product // products salsh that perticular product
Route::delete('/products/{product}', [ProductController::class, 'destroy'])->middleware('auth');
 
// Route::delete('/products/{product}', [ProductController::class, 'destroy'])->middleware('auth');








































// Manage Products
Route::get('/products/manage', [ProductController::class, 'manage'])->middleware('auth');

// Route::get('/products/manage', [ProductController::class, 'manage'])->middleware('auth');









































// Single Product
Route::get('/products/{id}', [ProductController::class, 'show']);


































// Show Register Create Form

Route::get('/register', [UserController::class, 'create'])->middleware('guest');
























// Create New User

Route::post('/users', [UserController::class, 'store']);

// Route::post('/users', [UserController::class, 'store']);













































// Log out User

Route::post('/logout', [UserController::class, 'logout'])->middleware('auth');

